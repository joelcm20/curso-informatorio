""" 9-Escribe un programa que solicite al usuario dos números y luego imprima la
suma, la resta, la multiplicación y la división de los dos números. """

print("Operaciones basicas a partir de dos numeros")
numero1 = int(input("Numero 1: "))
numero2 = int(input("Numero 2: "))

print(f"Suma: {numero1+numero2}")
print(f"Resta: {numero1-numero2}")
print(f"Multiplicacion: {numero1*numero2}")
print(f"division: {numero1/numero2}")